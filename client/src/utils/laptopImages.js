// Function to get random laptop image
export const getRandomLaptopImage = () => {
    const genericImages = [
        LAPTOP_IMAGES.laptop_generic_1,
        LAPTOP_IMAGES.laptop_generic_2,
        LAPTOP_IMAGES.laptop_generic_3,
        LAPTOP_IMAGES.laptop_generic_4,
        LAPTOP_IMAGES.laptop_generic_5,
        LAPTOP_IMAGES.laptop_generic_6,
        LAPTOP_IMAGES.laptop_generic_7
    ];
    
    return genericImages[Math.floor(Math.random() * genericImages.length)];
};

// Function to get brand-specific image
export const getBrandImage = (brand, model = '') => {
    const brandLower = brand.toLowerCase();
    const modelLower = model.toLowerCase();
    
    // Try to find specific model image
    const imageKey = `${brandLower}_${modelLower.split(' ')[0]}`;
    if (LAPTOP_IMAGES[imageKey]) {
        return LAPTOP_IMAGES[imageKey];
    }
    
    // Get first brand image
    const brandImages = Object.keys(LAPTOP_IMAGES)
        .filter(key => key.startsWith(brandLower))
        .map(key => LAPTOP_IMAGES[key]);
    
    if (brandImages.length > 0) {
        return brandImages[Math.floor(Math.random() * brandImages.length)];
    }
    
    // Fallback to generic
    return getRandomLaptopImage();
};

